package cn.disruptive.web.vo;

public class SceneGroupCountVo {
	
	private String sceneGroup;
	private String sceneGroupCode;
	private Integer count;
	private Integer succ;
	private Integer err;
	public String getSceneGroup() {
		return sceneGroup;
	}
	public void setSceneGroup(String sceneGroup) {
		this.sceneGroup = sceneGroup;
	}
	public String getSceneGroupCode() {
		return sceneGroupCode;
	}
	public void setSceneGroupCode(String sceneGroupCode) {
		this.sceneGroupCode = sceneGroupCode;
	}
	public Integer getCount() {
		return count;
	}
	public void setCount(Integer count) {
		this.count = count;
	}
	public Integer getSucc() {
		return succ;
	}
	public void setSucc(Integer succ) {
		this.succ = succ;
	}
	public Integer getErr() {
		return err;
	}
	public void setErr(Integer err) {
		this.err = err;
	}

}
